#!/usr/bin/env python3
"""Generate a 2-color heightmap bookmark from an image.

Writes heightmap.png and bookmark.scad to the output directory.
See CLAUDE.md for the print workflow.
"""

from __future__ import annotations

import argparse
import math
import shutil
import subprocess
import uuid
import zipfile
from datetime import date
from pathlib import Path

import numpy as np
from PIL import Image, ImageFilter

PX_PER_MM = 3
HOLE_DIAMETER_MM = 4.0
HOLE_FROM_TOP_MM = 7.0
DEFAULT_LEVELS = 6
DEFAULT_OPACITY_MM = 0.30
DEFAULT_MAX_SATURATION = 0.88

OPENSCAD_CANDIDATES = [
    r"C:\Program Files\OpenSCAD\openscad.exe",
    r"C:\Program Files (x86)\OpenSCAD\openscad.exe",
]


def build_heightmap(
    src: Path,
    width_mm: float,
    height_mm: float,
    bin_layers: list[int],
    contrast: float,
    blur: float,
    invert: bool,
    auto_contrast: float,
    fit: str,
) -> Image.Image:
    img = Image.open(src).convert("L")

    target_w = max(1, int(round(width_mm * PX_PER_MM)))
    target_h = max(1, int(round(height_mm * PX_PER_MM)))

    img_ratio = img.width / img.height
    target_ratio = target_w / target_h

    if fit == "cover":
        if img_ratio > target_ratio:
            new_h = target_h
            new_w = max(1, int(round(target_h * img_ratio)))
        else:
            new_w = target_w
            new_h = max(1, int(round(target_w / img_ratio)))
        img = img.resize((new_w, new_h), Image.LANCZOS)
        left = (new_w - target_w) // 2
        top = (new_h - target_h) // 2
        img = img.crop((left, top, left + target_w, top + target_h))
    else:  # contain
        if img_ratio > target_ratio:
            new_w = target_w
            new_h = max(1, int(round(target_w / img_ratio)))
        else:
            new_h = target_h
            new_w = max(1, int(round(target_h * img_ratio)))
        img = img.resize((new_w, new_h), Image.LANCZOS)

    if auto_contrast > 0:
        content = np.asarray(img, dtype=np.float32) / 255.0
        lo = np.percentile(content, auto_contrast)
        hi = np.percentile(content, 100 - auto_contrast)
        if hi > lo:
            content = np.clip((content - lo) / (hi - lo), 0.0, 1.0)
            img = Image.fromarray((content * 255).astype(np.uint8), mode="L")

    if fit == "contain":
        canvas = Image.new("L", (target_w, target_h), 0)
        canvas.paste(img, ((target_w - img.width) // 2, (target_h - img.height) // 2))
        img = canvas

    if blur > 0:
        img = img.filter(ImageFilter.GaussianBlur(radius=blur))

    arr = np.asarray(img, dtype=np.float32) / 255.0
    arr = (arr - 0.5) * contrast + 0.5
    arr = np.clip(arr, 0.0, 1.0)

    levels = len(bin_layers)
    quantized = np.floor(arr * levels).astype(np.int32)
    quantized = np.clip(quantized, 0, levels - 1)

    if invert:
        quantized = (levels - 1) - quantized

    top_layers = bin_layers[-1]
    lut = np.array(
        [round(b / top_layers * 255) for b in bin_layers], dtype=np.uint8
    )
    scaled = lut[quantized]
    return Image.fromarray(scaled, mode="L")


SCAD_TEMPLATE = """\
// Generated by bookmark_heightmap.py
// Single-body bookmark. Print on Bambu X1C with AMS:
//   1. Render (F6), Export STL.
//   2. Import bookmark.stl into BambuStudio.
//   3. Filament 1 = dark, Filament 2 = light.
//   4. Add a filament change at layer {swap_layer}
//      (= base_thickness / layer_height). Below that = dark; above = light.

width          = {width};
height         = {height};
base_thickness = {base};
layer_height   = {layer};
top_layers     = {top_layers};
hole_diameter  = {hole_d};
hole_inset     = {hole_y};

surface_max_z = top_layers * layer_height;
total_max_z   = base_thickness + surface_max_z;

// Hole goes on the short edge (the "end" where the tassel hangs).
// Portrait: hole at top short edge. Landscape: hole at right short edge.
module hole() {{
    pos = (width > height)
        ? [width/2 - hole_inset, 0]
        : [0, height/2 - hole_inset];
    translate([pos[0], pos[1], -1])
        cylinder(h = total_max_z + 2, d = hole_diameter, $fn = 64);
}}

module bookmark() {{
    difference() {{
        union() {{
            translate([-width/2, -height/2, 0])
                cube([width, height, base_thickness]);
            translate([0, 0, base_thickness])
                resize([width, height, surface_max_z])
                    surface(file = "heightmap.png", center = true, convexity = 5);
        }}
        hole();
    }}
}}

bookmark();
"""


def write_scad(
    out: Path,
    width: float,
    height: float,
    base: float,
    layer: float,
    top_layers: int,
) -> int:
    swap_layer = max(1, round(base / layer))
    content = SCAD_TEMPLATE.format(
        width=width,
        height=height,
        base=base,
        layer=layer,
        top_layers=top_layers,
        swap_layer=swap_layer,
        hole_d=HOLE_DIAMETER_MM,
        hole_y=HOLE_FROM_TOP_MM,
    )
    out.write_text(content)
    return swap_layer


def find_openscad() -> str | None:
    found = shutil.which("openscad")
    if found:
        return found
    for path in OPENSCAD_CANDIDATES:
        if Path(path).exists():
            return path
    return None


def render_stl(scad: Path, stl: Path, openscad_exe: str) -> None:
    subprocess.run(
        [openscad_exe, "-o", str(stl.resolve()), str(scad.name)],
        check=True,
        cwd=scad.parent,
    )


def parse_ascii_stl(
    path: Path,
) -> tuple[list[tuple[float, float, float]], list[tuple[int, int, int]]]:
    verts: dict[tuple[float, float, float], int] = {}
    tris: list[tuple[int, int, int]] = []
    cur: list[int] = []
    with open(path) as f:
        for line in f:
            stripped = line.strip()
            if stripped.startswith("vertex"):
                _, x, y, z = stripped.split()
                v = (float(x), float(y), float(z))
                idx = verts.get(v)
                if idx is None:
                    idx = len(verts)
                    verts[v] = idx
                cur.append(idx)
                if len(cur) == 3:
                    tris.append((cur[0], cur[1], cur[2]))
                    cur = []
    return list(verts), tris



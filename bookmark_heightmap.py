def build_object_mesh_xml(
    vertices: list[tuple[float, float, float]],
    triangles: list[tuple[int, int, int]],
) -> str:
    v_lines = "\n".join(
        f'     <vertex x="{x:.6f}" y="{y:.6f}" z="{z:.6f}"/>'
        for x, y, z in vertices
    )
    t_lines = "\n".join(
        f'     <triangle v1="{a}" v2="{b}" v3="{c}"/>' for a, b, c in triangles
    )
    return f"""<?xml version="1.0" encoding="UTF-8"?>
<model unit="millimeter" xml:lang="en-US" xmlns="http://schemas.microsoft.com/3dmanufacturing/core/2015/02">
 <resources>
  <object id="1" type="model">
   <mesh>
    <vertices>
{v_lines}
    </vertices>
    <triangles>
{t_lines}
    </triangles>
   </mesh>
  </object>
 </resources>
</model>
"""


def build_custom_gcode_xml(top_z: float) -> str:
    return f"""<?xml version="1.0" encoding="utf-8"?>
<custom_gcodes_per_layer>
<plate>
<plate_info id="1"/>
<layer top_z="{top_z}" type="2" extruder="2" color="#FFFFFF" extra="" gcode="tool_change"/>
<mode value="MultiAsSingle"/>
</plate>
</custom_gcodes_per_layer>
"""


def write_3mf(out: Path, stl: Path, swap_top_z: float) -> None:
    verts, tris = parse_ascii_stl(stl)
    mesh_xml = build_object_mesh_xml(verts, tris)
    custom_gcode = build_custom_gcode_xml(swap_top_z)

    with zipfile.ZipFile(out, "w", zipfile.ZIP_DEFLATED) as z:
        z.writestr("[Content_Types].xml", CONTENT_TYPES_XML)
        z.writestr("_rels/.rels", ROOT_RELS_XML)
        z.writestr("3D/3dmodel.model", build_main_model_xml())
        z.writestr("3D/_rels/3dmodel.model.rels", MODEL_RELS_XML)
        z.writestr("3D/Objects/object_1.model", mesh_xml)
        z.writestr("Metadata/model_settings.config", MODEL_SETTINGS_XML)
        z.writestr(
            "Metadata/project_settings.config",
            PROJECT_SETTINGS_TEMPLATE.read_text(),
        )
        z.writestr("Metadata/filament_sequence.json", FILAMENT_SEQUENCE_JSON)
        z.writestr("Metadata/slice_info.config", SLICE_INFO_XML)
        z.writestr("Metadata/custom_gcode_per_layer.xml", custom_gcode)


def derive_bin_layers(
    levels: int,
    opacity_mm: float,
    max_saturation: float,
    layer_height_mm: float,
) -> list[int]:
    """Beer-Lambert bins for perceptually even brightness steps.

    Models reflectance as L(d) = L_max * (1 - exp(-d / opacity_mm)). To get N
    evenly-spaced perceived steps from 0 to max_saturation * L_max, invert:
        d_i = -opacity_mm * ln(1 - max_saturation * i / (N - 1))
    Then snap each d_i to the nearest integer multiple of layer_height_mm.
    """
    if not 2 <= levels <= 8:
        raise ValueError("levels must be 2-8")
    if opacity_mm <= 0:
        raise ValueError("opacity-mm must be positive")
    if not 0 < max_saturation < 1:
        raise ValueError("max-saturation must be in (0, 1) exclusive")

    bins: list[int] = []
    for i in range(levels):
        frac = max_saturation * i / (levels - 1)
        d = 0.0 if frac == 0 else -opacity_mm * math.log(1 - frac)
        bins.append(round(d / layer_height_mm))

    for i in range(1, len(bins)):
        if bins[i] <= bins[i - 1]:
            raise ValueError(
                f"Derived bin_layers={bins} not strictly increasing at "
                f"index {i}. Try fewer --levels, larger --max-saturation, "
                f"smaller --opacity-mm, or finer --layer."
            )
    return bins


def parse_bin_layers(s: str) -> list[int]:
    try:
        layers = [int(x.strip()) for x in s.split(",") if x.strip()]
    except ValueError:
        raise argparse.ArgumentTypeError(
            "--bin-layers must be comma-separated integers"
        )
    if not 2 <= len(layers) <= 8:
        raise argparse.ArgumentTypeError("--bin-layers must have 2-8 entries")
    if layers[0] != 0:
        raise argparse.ArgumentTypeError("--bin-layers first value must be 0")
    if any(layers[i] >= layers[i + 1] for i in range(len(layers) - 1)):
        raise argparse.ArgumentTypeError(
            "--bin-layers must be strictly increasing"
        )
    return layers


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("input", type=Path, help="Source image (any PIL-readable format)")
    p.add_argument("--width", type=float, default=50, help="Bookmark width mm")
    p.add_argument("--height", type=float, default=150, help="Bookmark height mm")
    p.add_argument(
        "--levels",
        type=int,
        default=DEFAULT_LEVELS,
        help=f"Number of tonal bins, 2-8 (default: {DEFAULT_LEVELS}). "
             "Ignored if --bin-layers is given.",
    )
    p.add_argument(
        "--opacity-mm",
        type=float,
        default=DEFAULT_OPACITY_MM,
        help=f"Light-filament characteristic opacity length in mm "
             f"(default: {DEFAULT_OPACITY_MM}). See CLAUDE.md for "
             "calibration. Ignored if --bin-layers is given.",
    )
    p.add_argument(
        "--max-saturation",
        type=float,
        default=DEFAULT_MAX_SATURATION,
        help=f"Brightest tone's fraction of asymptotic saturation, "
             f"0<s<1 (default: {DEFAULT_MAX_SATURATION}). "
             "Ignored if --bin-layers is given.",
    )
    p.add_argument(
        "--bin-layers",
        type=parse_bin_layers,
        default=None,
        help="Explicit override: layer count per tonal bin, comma-separated, "
             "ascending, starts at 0. When set, --levels/--opacity-mm/"
             "--max-saturation are ignored.",
    )
    p.add_argument("--layer", type=float, default=0.08, help="Layer height mm")
    p.add_argument("--base", type=float, default=0.8, help="Base thickness mm")
    p.add_argument("--contrast", type=float, default=1.3, help="Contrast boost, >1.0")
    p.add_argument(
        "--auto-contrast",
        type=float,
        default=1.0,
        help="Percentile to clip on each end before stretching to full range. "
             "0 disables. Default 1.0 (clips brightest/darkest 1%% as outliers).",
    )
    p.add_argument("--blur", type=float, default=0.5, help="Gaussian blur radius px")
    p.add_argument(
        "--fit",
        choices=["cover", "contain"],
        default="cover",
        help="How to fit source aspect to bookmark aspect. cover (default) "
             "scales to fill and center-crops the overflow. contain scales "
             "to fit and letterboxes with dark padding (use for calibration "
             "prints where edge content matters).",
    )
    p.add_argument("--invert", action="store_true", help="Flip dark/light mapping")
    p.add_argument("--out", type=Path, default=Path("."), help="Output directory")
    p.add_argument(
        "--no-3mf",
        action="store_true",
        help="Skip auto-rendering STL and 3MF (just write SCAD)",
    )
    args = p.parse_args()

    args.out.mkdir(parents=True, exist_ok=True)

    if args.bin_layers is None:
        try:
            bin_layers = derive_bin_layers(
                args.levels,
                args.opacity_mm,
                args.max_saturation,
                args.layer,
            )
        except ValueError as e:
            p.error(str(e))
        print(f"derived bin-layers: {','.join(str(b) for b in bin_layers)}")
    else:
        bin_layers = args.bin_layers

    hm = build_heightmap(
        args.input,
        args.width,
        args.height,
        bin_layers,
        args.contrast,
        args.blur,
        args.invert,
        args.auto_contrast,
        args.fit,
    )
    hm_path = args.out / "heightmap.png"
    hm.save(hm_path)

    scad_path = args.out / "bookmark.scad"
    swap_layer = write_scad(
        scad_path,
        args.width,
        args.height,
        args.base,
        args.layer,
        bin_layers[-1],
    )

    print(f"wrote {hm_path}")
    print(f"wrote {scad_path}")

    if args.no_3mf:
        print(f"BambuStudio: add filament change at layer {swap_layer} "
              f"(dark below, light above)")
        return

    openscad = find_openscad()
    if not openscad:
        print("OpenSCAD not found; skipping STL/3MF (use --no-3mf to silence).")
        print(f"BambuStudio: add filament change at layer {swap_layer} "
              f"(dark below, light above)")
        return

    stl_path = args.out / "bookmark.stl"
    print(f"rendering STL via {openscad}...")
    render_stl(scad_path, stl_path, openscad)
    print(f"wrote {stl_path}")

    threemf_path = args.out / "bookmark.3mf"
    write_3mf(threemf_path, stl_path, swap_top_z=args.base)
    print(f"wrote {threemf_path}")
    print("Open bookmark.3mf in BambuStudio; the filament change is pre-set.")


if __name__ == "__main__":
    main()
